import random
import os
import sys
import math

if (len(sys.argv) < 2 or (sys.argv[1] == "")):
    distance = 0.8
else:
    distance = float(sys.argv[1])

filename = os.path.basename(__file__)

wall_angle = 45  # Modify this value to change the angle of the wall
box_width = 0.05
wall_length = 40
wall_height = 20

# Calculate the y-offset based on the difference in y between the first and last box
y_offset = (wall_height - 1) * box_width * math.sin(math.radians(wall_angle))

xml_elements = ""
for j in range(wall_height):
    z = (j * -box_width) + 1
    for i in range(wall_length):
        if (i + j) % 2 == 0:
            color = "1 1 1"  # white
        else:
            color = "0 0 0"  # black
        x = (i * box_width) - .95
        y = (distance + j * box_width * math.sin(math.radians(wall_angle))) - y_offset
        xml_elements += f'<geom type="box" size=".025 .025 .03" pos="{x} {y} {z}" euler="{90 - wall_angle} 0 0" rgba="{color} 1" />\n'

xml_file = f'''
<!-- This file was generated by build_angled_wall.py -->
<!-- It is not recommended to edit this file -->
<mujoco>
    <default>
        <geom density="100" friction=".5" rgba="1 .6 .8 1" />
    </default>
    <worldbody>
        {xml_elements}
    </worldbody>
</mujoco>
'''

# Save the XML file
with open('model/walls/angled_wall.xml', 'w') as f:
    f.write(xml_file)

print(f'Complete. ({filename}))')
